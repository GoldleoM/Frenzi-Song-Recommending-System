def find_song(query, artist=None):
    """Helper to find a song by name, returning its row with prioritized matching."""
    q = query.strip()
    
    # 1. Exact Match (Case-Insensitive)
    exact_matches = df[df['track_name'].str.fullmatch(re.escape(q), case=False, na=False)]
    if artist:
        # Filter by artist if provided
        artist_matches = exact_matches[exact_matches['artists'].str.contains(re.escape(artist), case=False, na=False)]
        if not artist_matches.empty: return artist_matches.sort_values('popularity', ascending=False).iloc[0]
    elif not exact_matches.empty:
        return exact_matches.sort_values('popularity', ascending=False).iloc[0]

    # 2. Starts-With Match (Case-Insensitive)
    starts_matches = df[df['track_name'].str.startswith(q, na=False)]
    if not starts_matches.empty:
        return starts_matches.sort_values('popularity', ascending=False).iloc[0]

    # 3. Substring Match (Case-Insensitive)
    substring_matches = df[df['track_name'].str.contains(re.escape(q), case=False, na=False)]
    if not substring_matches.empty:
        return substring_matches.sort_values('popularity', ascending=False).iloc[0]
    
    # If no match, raise error
    raise ValueError(f"Song '{query}' not found in dataset.")

def notebook_recommend(song_name, n=5, w_audio=0.3, w_genre=0.7, exclude=None):
    """Recommends similar songs with duplicate filtering and returns a style-ready DataFrame."""
    # 1. Find the target song
    target_song = find_song(song_name)
    target_idx = target_song.name
    
    # 2. Extract vectors
    audio_cols = [col for col in df_final.columns if col.startswith('num__')]
    genre_cols = [col for col in df_final.columns if col.startswith('cat__')]
    
    target_audio = df_final.loc[[target_idx], audio_cols]
    target_genre = df_final.loc[[target_idx], genre_cols]
    
    # 3. Calculate similarities on-the-fly
    audio_sims = cosine_similarity(target_audio, df_final[audio_cols])[0]
    genre_sims = cosine_similarity(target_genre, df_final[genre_cols])[0]
    
    # 4. Weighted combination
    total_sims = (w_audio * audio_sims) + (w_genre * genre_sims)
    
    # 5. Filter & Deduplicate
    results = df.copy()
    results['similarity_score'] = total_sims
    results = results.drop(target_idx)
    candidates = results.sort_values('similarity_score', ascending=False).head(n * 2)
    
    final_list = []
    seen = set()
    # Add the target song to seen so it doesn't appear in recommendations
    seen.add((normalize_title(target_song['track_name']), normalize_artist(target_song['artists'])))
    # Also exclude previously played songs from appearing in recommendations
    if exclude:
        for ex_name in exclude:
            seen.add((normalize_title(ex_name), ''))
    
    for _, row in candidates.iterrows():
        norm_t = normalize_title(row['track_name'])
        norm_a = normalize_artist(row['artists'])
        # Check both exact (title, artist) match and title-only match for excluded songs
        if (norm_t, norm_a) not in seen and (norm_t, '') not in seen:
            final_list.append(row)
            seen.add((norm_t, norm_a))
            if len(final_list) == n: break
                
    top_n = pd.DataFrame(final_list)
    
    # 6. Add Meta-data
    reasons = []
    for _, row in top_n.iterrows():
        if row['track_genre'] == target_song['track_genre']:
            reasons.append('Matches genre and audio profile')
        elif abs(row['energy'] - target_song['energy']) < 0.1:
            reasons.append('Very similar energy and vibe')
        else:
            reasons.append('Similar sonic qualities')
            
    top_n['Reason'] = reasons
    top_n['Similarity %'] = (top_n['similarity_score'] * 100).round(1)
    
    return top_n, target_song


