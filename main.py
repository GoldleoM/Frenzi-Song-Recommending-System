def fetch_youtube_as_recommendation(song_name: str, n: int, exclude: list[str] = None):
    yt_api_key = os.environ.get("YOUTUBE_API_KEY")
    if not yt_api_key or yt_api_key == "YOUR_YOUTUBE_DATA_API_KEY":
        return {"error": "YouTube fallback disabled (Missing API Key)"}

    # 1. Search YouTube for metadata
    try:
        response = requests.get(
            "https://www.googleapis.com/youtube/v3/search",
            params={
                "q": f"{song_name} audio",
                "part": "snippet",
                "maxResults": 1,
                "type": "video",
                "key": yt_api_key
            }
        )
        if response.status_code != 200:
            return {"error": "YouTube search failed"}
        
        yt_res = response.json()
        if not yt_res.get("items"):
            return {"error": f"Could not find '{song_name}' on YouTube"}

        yt_item = yt_res["items"][0]
        track_name = yt_item["snippet"]["title"]
        artist_name = yt_item["snippet"]["channelTitle"].replace(" - Topic", "")
        image_url = yt_item["snippet"]["thumbnails"]["high"]["url"] if "high" in yt_item["snippet"]["thumbnails"] else yt_item["snippet"]["thumbnails"]["default"]["url"]
        
        # 2. Surrogate Recommendations
        # Strategy: Find songs in OUR dataset by the same artist
        artist_matches = df[df['artists'].str.contains(artist_name.split()[0], case=False, na=False)]
        
        if not artist_matches.empty:
            # Pick their most popular song as a surrogate seed
            surrogate = artist_matches.sort_values('popularity', ascending=False).iloc[0]
            top_n, _ = notebook_recommend(surrogate['track_name'], n, exclude=exclude)
        else:
            # If no artist match, return globally popular songs
            top_n = df.sort_values('popularity', ascending=False).head(n)
            # Add dummy columns for compatibility
            top_n = top_n.copy()
            top_n['similarity_score'] = 0.0
            top_n['Similarity %'] = "0"
            top_n['Reason'] = "Trending Track"

        results = []
        for _, row in top_n.iterrows():
            sim_val = row.get('similarity_score', 0)
            sim_pct = row.get('Similarity %', round(sim_val * 100, 1))
            results.append({
                "track_name": row['track_name'],
                "artists": row['artists'],
                "album_name": row['album_name'],
                "track_genre": row['track_genre'],
                "similarity": float(sim_val),
                "similarity_percent": str(sim_pct),
                "reason": row.get('Reason', "Trending Track")
            })

        return {
            "query": song_name,
            "matched_song": track_name,
            "matched_artists": artist_name,
            "image_url": image_url,
            "recommendations": results,
            "fallback": True
        }
    except Exception as e:
        return {"error": str(e)}


def enrich_recommendations_with_cache(recommendations):
    """Checks each recommendation against local & Firestore cache to provide instant thumbnails/IDs."""
    enriched = []
    for rec in recommendations:
        norm_t = normalize_string_for_cache(rec['track_name'])
        norm_a = normalize_string_for_cache(rec['artists'])
        check_query = f"{norm_t} {norm_a} official audio"
        check_id = hashlib.md5(check_query.encode('utf-8')).hexdigest()
        
        cached_id = None
        if check_id in PLAY_CACHE:
            cached_id = PLAY_CACHE[check_id]
        elif FIRESTORE_AVAILABLE:
            try:
                snap = db.collection('youtube_cache').document(check_id).get()
                if snap.exists:
                    cached_id = snap.to_dict().get('videoId')
                    PLAY_CACHE[check_id] = cached_id
            except: pass
        
        rec['videoId'] = cached_id
        enriched.append(rec)
    return enriched

@app.post("/recommend")
def recommend_songs(req: RecommendRequest):
    song_name = req.song_name
    n = req.n
    
    try:
        # Use artist hint if available to ensure accurate selection from autocomplete
        target_song = find_song(song_name, artist=req.artist)
    except ValueError:
        print(f"'{song_name}' not in local memory. Falling back to YouTube search...", flush=True)
        yt_data = fetch_youtube_as_recommendation(song_name, n, exclude=req.exclude)
        if 'recommendations' in yt_data:
            yt_data['recommendations'] = enrich_recommendations_with_cache(yt_data['recommendations'])
        return yt_data
        
    # If it exists, use native notebook execution!
    top_n, t_song = notebook_recommend(song_name, n, exclude=req.exclude)
    
    results = []
    for _, row in top_n.iterrows():
        results.append({
            "track_name": row['track_name'],
            "artists": row['artists'],
            "album_name": row['album_name'],
            "track_genre": row['track_genre'],
            "similarity": row['similarity_score'],
            "similarity_percent": row['Similarity %'],
            "reason": row['Reason']
        })
    
    # Enrich with cache before returning
    results = enrich_recommendations_with_cache(results)
        
    return {
        "query": song_name,
        "matched_song": t_song['track_name'],
        "matched_artists": t_song['artists'],
        "image_url": None, 
        "recommendations": results,
        "source": "local",
        "fallback": False
    }

@app.get("/search")
def search_suggestions(q: str = "", limit: int = 8):
    """Fast autocomplete from the local dataset — no external API."""
    if not q or len(q.strip()) < 2:
        return {"suggestions": []}
    
    query = q.strip()
    results = []
    seen = set()

    # 1. Local Dataset Matches (Substring)
    matches = df[df['track_name'].str.contains(query, case=False, na=False, regex=False)]
    if not matches.empty:
        matches = matches.sort_values('popularity', ascending=False)
        for _, row in matches.iterrows():
            key = (normalize_title(row['track_name']), normalize_artist(row['artists']))
            if key not in seen:
                seen.add(key)
                results.append({
                    "track_name": row['track_name'],
                    "artists": row['artists'],
                    "album_name": row['album_name'],
                    "track_genre": row['track_genre'],
                    "popularity": int(row['popularity']),
                })
            if len(results) >= limit: break

    # 2. Firestore Cache Matches (Prefix - for discovered tracks)
    if FIRESTORE_AVAILABLE and len(results) < limit:
        try:
            cache_query = db.collection('youtube_cache')\
                .where('raw_track', '>=', query)\
                .where('raw_track', '<=', query + '\uf8ff')\
                .limit(limit)\
                .get()
            
            for doc in cache_query:
                doc_data = doc.to_dict()
                nt = normalize_title(doc_data.get('raw_track', ''))
                na = normalize_artist(doc_data.get('raw_artist', ''))
                key = (nt, na)
                if key not in seen:
                    seen.add(key)
                    results.append({
                        "track_name": doc_data.get('raw_track'),
                        "artists": doc_data.get('raw_artist'),
                        "album_name": "Cached Discovery",
                        "track_genre": "External",
                        "popularity": 0 
                    })
                if len(results) >= limit: break
        except Exception as e:
            print(f"Firestore suggestion error: {e}")
    
    return {"suggestions": results[:limit]}


@app.post("/lyrics")
def fetch_lyrics(req: LyricsRequest):
    """Fetches full lyrics from Genius on-demand."""
    token = os.environ.get("GENIUS_ACCESS_TOKEN")
    if not token or token == "PASTE_YOUR_TOKEN_HERE":
        return {"error": "Genius API Access Token missing. Add it to .env in the functions/ folder."}

    try:
        # Initialize Genius with the token
        # We disable verbose and exclude non-songs to keep it fast
        genius = lyricsgenius.Genius(token)
        genius.verbose = False
        genius.remove_section_headers = False # Keep them for now as they help structure

        # Search for the song
        # We use a combined query for accuracy
        song = genius.search_song(req.track_name, req.artist)
        
        if not song:
            return {"error": f"Lyrics not found for '{req.track_name}' by {req.artist}"}

        # Clean up the lyrics text slightly
        # Genius often adds a "Lyrics" prefix and "Embed" suffix
        raw_lyrics = song.lyrics
        
        # Simple cleanup of Genius specific artifacts
        cleaned = re.sub(r'^[0-9]+ Lyrics', '', raw_lyrics) # Remove leading "X Lyrics"
        cleaned = re.sub(r'Embed$', '', cleaned)            # Remove trailing "Embed"
        cleaned = re.sub(r'[0-9]*Embed$', '', cleaned)       # Sometimes it's number-Embed
        
        return {
            "track_name": song.title,
            "artist": song.artist,
            "lyrics": cleaned,
            "source": "genius",
            "url": song.url
        }
    except Exception as e:
        print(f"Lyrics Error: {e}")
        return {"error": f"Failed to fetch lyrics: {str(e)}"}


