PLAY_CACHE = {}

class PlayRequest(BaseModel):
    track_name: str
    artists: str

def normalize_string_for_cache(s):
    # Lowercase, strip spaces, remove (Remix), - Live, etc for max collisions
    s = str(s).lower()
    s = re.sub(r'\(.*?\)', '', s)
    s = re.sub(r'- live.*?', '', s)
    s = re.sub(r'- radio edit.*?', '', s)
    s = re.sub(r'- remix.*?', '', s)
    return s.strip()

@app.post("/play")
def fetch_youtube_playback(req: PlayRequest):
    norm_track = normalize_string_for_cache(req.track_name)
    norm_artist = normalize_string_for_cache(req.artists)
    
    query = f"{norm_track} {norm_artist} official audio"
    doc_id = hashlib.md5(query.encode('utf-8')).hexdigest()
    
    # 1. Try Memory Cache
    if doc_id in PLAY_CACHE:
        return {"videoId": PLAY_CACHE[doc_id], "source": "memory"}
        
    # 2. Try Firestore Cache
    if FIRESTORE_AVAILABLE:
        try:
            doc_ref = db.collection('youtube_cache').document(doc_id)
            doc_snap = doc_ref.get()
            if doc_snap.exists:
                vid = doc_snap.to_dict().get('videoId')
                PLAY_CACHE[doc_id] = vid # Warm up local cache
                return {"videoId": vid, "source": "firestore"}
        except Exception as e:
            print(f"Firestore read error: {e}")
            
    yt_api_key = os.environ.get("YOUTUBE_API_KEY")
    if not yt_api_key or yt_api_key == "YOUR_YOUTUBE_DATA_API_KEY":
        return {"error": "Native playback is disabled (Missing YOUTUBE_API_KEY in .env)"}
        
    # 3. YouTube API Fallback
    try:
        response = requests.get(
            "https://www.googleapis.com/youtube/v3/search",
            params={
                "q": query,
                "part": "snippet",
                "maxResults": 1,
                "type": "video",
                "videoEmbeddable": "true", # CRITICAL: Only find videos we can actually play
                "videoSyndicated": "true",
                "key": yt_api_key
            }
        )
        
        if response.status_code == 403:
            return {"error": "YouTube API Quota Exceeded. Please try again tomorrow or use a different key."}
            
        if response.status_code == 200:
            data = response.json()
            if data.get("items"):
                vid = data["items"][0]["id"]["videoId"]
                
                # Proactively set cache
                PLAY_CACHE[doc_id] = vid
                if FIRESTORE_AVAILABLE:
                    try:
                        db.collection('youtube_cache').document(doc_id).set({
                            'videoId': vid, 
                            'query': query, 
                            'raw_track': req.track_name, 
                            'raw_artist': req.artists,
                            'timestamp': firestore.SERVER_TIMESTAMP
                        })
                    except Exception as e:
                        print(f"Firestore write error: {e}")
                        
                return {"videoId": vid, "source": "youtube_api"}
        
        # If the restrictive query failed, try a broader search once
        if "official audio" in query:
            broad_query = f"{req.track_name} {req.artists}"
            response = requests.get(
                "https://www.googleapis.com/youtube/v3/search",
                params={
                    "q": broad_query,
                    "part": "snippet",
                    "maxResults": 1,
                    "type": "video",
                    "videoEmbeddable": "true",
                    "key": yt_api_key
                }
            )
            if response.status_code == 200:
                data = response.json()
                if data.get("items"):
                    vid = data["items"][0]["id"]["videoId"]
                    return {"videoId": vid, "source": "youtube_api_broad"}

        return {"error": f"Video not found or is restricted. (Status: {response.status_code})"}
    except Exception as e:
        return {"error": f"Playback error: {str(e)}"}

